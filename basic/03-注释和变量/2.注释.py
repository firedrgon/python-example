# 2. 提高自己在后续的项目迭代中的可读性和开发效率. Python中有两种形式的注释:
'''
1. 单行注释:以# 开头，可以注释掉从# 开始后面一整行的内容。
2. 多行注释:三个引号开头，三个引号结尾，通常用于添加多行说明性内容。引号包括单引号和双引
  号两种
注意: python中的注释不会被python的解释器执行.
'''

