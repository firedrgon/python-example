# 代码结构:  顺序结构  分支结构   循环结构

# 顺序结构: 代码从上往下依次执行
'''
a = 12
b = 43
print(a)  # 12
print(b)  # 43
'''
# 分支结构: 根据不同的条件,执行不同的语句
# 单分支语句:
# 语法:
'''
if 表达式:
    语句
执行流程: 当表达式成立(表达式为True)时,执行里面的 语句. 当表达式不成立时,不执行里面的语句.
'''
num1 = 12
num2 = 9
# 表达式成立的情况:
if num1 > num2:
    print('num1是一个比num2大的数字')

print('hello world')
# 表达式不成立的情况:
if num1 < num2:
    print('num1是一个比num2小的数字')
print('你在他乡还好吗?')




