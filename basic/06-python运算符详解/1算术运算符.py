# 常见的算术运算符: + - * / % // **

num1 = 24
num2 = 2
num3 = 9
# 加法运算
print(num1 + num2)   # 26
# 减法运算
print(num1 - num2)   # 22
# 乘法运算
print(num1 * num2)   # 48
# 除法运算
print(num1 / num2)   # 12
# 取余或者取模运算 (相除之后的余数)
print(num1 % num2)   # 0
print(num1 % num3)   # 6
# 幂运算
print(num1 ** num2)  # 576
# 取整运算(取的是相除之后的商)
print(num1 // num3)  # 2
